namespace UnitySocial
{
namespace Entities
{
    /// <summary>
    /// Represents a user in Unity Social
    /// </summary>
    public class User
    {
        /// <summary>
        /// Unique identifier of the user
        /// </summary>
        public string id;

        /// <summary>
        /// The username of the user
        /// </summary>
        public string username;

        /// <summary>
        /// URL of the avatar image of the user
        /// </summary>
        public string avatarURL;
    }
}
}
